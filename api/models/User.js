const mongoose = require("mongoose");

//define user schema as:-
const UserSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    profilePic: {
      type: String,
      default: "",
    },
  },
  { timestamps: true } //we generate updated or created user time
);

module.exports = mongoose.model("User", UserSchema);
